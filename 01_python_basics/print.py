# 1️⃣ Basic print
print("Hello, World!")

# 2️⃣ Printing variables
name = "Laraib"
age = 24
print("Name:", name)
print("Age:", age)

# 3️⃣ String concatenation
print("My name is " + name + " and I am " + age + " years old.")

# 4️⃣ Formatted strings (f-strings)
print(f"My name is {name} and I am {age} years old.")

# 5️⃣ Escape characters
print("Hello\nWorld")     # New line
print("Python\tBeginner") # Tab space

# 6️⃣ Printing multiple items
x = 10
y = 20
print("x =", x, "y =", y, "Sum =", x + y)
